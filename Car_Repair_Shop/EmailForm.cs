using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Windows.Forms;
using System.Net.Mail;

namespace Car_Repair_Shop
{
    public class EmailForm
    {
        internal EmailForm(string Subject, string To, string Message)
        {
            Send(Subject, To, Message);
        }

        private void Send(string Subject, string To, string Message)
        {
            try
            {
                MailMessage mail = new MailMessage();
                SmtpClient SmtpServer = new SmtpClient("smtp.gmail.com");

                mail.From = new MailAddress("carrepairshop123@gmail.com");
                mail.To.Add(To);
                mail.Subject = Subject;
                mail.Body = Message + " \nEmail generated by: Car Repair Shop ";

                SmtpServer.Port = 587;
                SmtpServer.Credentials = new System.Net.NetworkCredential("carrepairshop123@gmail.com", "Car_repair123");
                SmtpServer.EnableSsl = true;

                SmtpServer.Send(mail);
            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.ToString());
            }
        }
    }
}
